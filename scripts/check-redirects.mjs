#!/usr/bin/env node
// check-redirects.mjs — the dev→help URL contract, as a checkable artifact.
//
// WHY
//   developers.keboola.com retirement (PRDCT-565) needs one consolidated
//   "every dev URL → final help URL" table; today that truth is split across
//   PLACEMENT-MAP.md, MIGRATION-REPORT(.md/-phase2.md) and batch PRs, and the
//   in-build redirects are meta-refresh pages, not 301 rules. This script makes
//   the table a build artifact and checks it three ways.
//
// USAGE
//   node scripts/check-redirects.mjs --gen        # (re)generate _data/redirects/dev-to-help.tsv from PLACEMENT-MAP.md
//   node scripts/check-redirects.mjs --build      # verify against dist/ (run `npm run build` first):
//                                                 #   every dev path must resolve (real page or redirect page) — else FAIL
//   node scripts/check-redirects.mjs --flippable  # absolute developers.keboola.com links in content whose target already lives on help
//   node scripts/check-redirects.mjs --live       # cutover day: HTTP developers.keboola.com/<path> must 301 to the mapped help URL
//
// TSV columns: dev_path <TAB> final_help_path <TAB> status
//   moved    — page gets a topic home on help (final_help_path)
//   redirect — dev landing/duplicate; 301 straight to the canonical
//   dies     — site chrome (404, homepage) — no mapping required, listed for completeness
//   unsure   — PLACEMENT-MAP row awaiting a PRDCT-550 call; final path is the proposal

import fs from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const MAP_MD = path.join(ROOT, 'PLACEMENT-MAP.md');
const TSV = path.join(ROOT, '_data', 'redirects', 'dev-to-help.tsv');
const DIST = path.join(ROOT, 'dist');
const DEV_HOST = 'https://developers.keboola.com';
const HELP_HOST = 'https://help.keboola.com';

const mode = process.argv[2];
const clean = (p) => {
  if (!p || p === '—' || p === '-') return null;
  let s = p.replace(/`/g, '').trim();
  if (!s.startsWith('/')) return null;
  if (!s.endsWith('/') && !/\.[a-z]+$/.test(s)) s += '/';
  return s;
};

function parsePlacementMap() {
  const rows = [];
  for (const line of fs.readFileSync(MAP_MD, 'utf8').split('\n')) {
    const m = line.match(/^\| `([^`]+\.md)` \| `([^`]+)` \| ([^|]+) \| ([^|]*) \|([^|]*)\|([^|]*)\|(.*)\|?\s*$/);
    if (!m) continue;
    const [, , oldUrl, section, target, , conf, dedup] = m;
    const dev = clean(oldUrl); // clean() keeps .html forms as-is
    if (!dev) continue;
    const tgt = clean(target);
    let status;
    if (/NOT MIGRATED/i.test(dedup) && !tgt) status = 'dies';
    else if (/REDIRECT/i.test(dedup) || section.trim().startsWith('—')) status = 'redirect';
    else status = 'moved';
    if (/UNSURE/i.test(dedup) || /UNSURE/i.test(section)) status = 'unsure';
    rows.push({ dev, tgt: tgt ?? '', status, conf: conf.trim() });
  }
  return rows;
}

function gen() {
  const rows = parsePlacementMap();
  // PLACEMENT-MAP asserts 175/175 coverage — any parse below that is silent row loss
  if (rows.length < 175) fail(`parsed only ${rows.length}/175 rows from PLACEMENT-MAP.md — format drifted, refusing to write a lossy map`);
  fs.mkdirSync(path.dirname(TSV), { recursive: true });
  const body = rows.map((r) => `${r.dev}\t${r.tgt}\t${r.status}`).join('\n');
  fs.writeFileSync(TSV, `# generated by scripts/check-redirects.mjs --gen from PLACEMENT-MAP.md — do not hand-edit\n# dev_path\tfinal_help_path\tstatus\n${body}\n`);
  const c = count(rows.map((r) => r.status));
  console.log(`✓ ${TSV.replace(ROOT + '/', '')}: ${rows.length} rows (${fmt(c)})`);
}

function readTsv() {
  if (!fs.existsSync(TSV)) fail('map missing — run --gen first');
  return fs.readFileSync(TSV, 'utf8').split('\n')
    .filter((l) => l && !l.startsWith('#'))
    .map((l) => { const [dev, tgt, status] = l.split('\t'); return { dev, tgt, status }; });
}

// a path "resolves" in dist if a page (real or redirect stub) exists there
const inDist = (p) => {
  if (!p) return false;
  const c = p.replace(/^\//, '').replace(/\/$/, '');
  return ['index.html', '.html'].some((suf) =>
    fs.existsSync(path.join(DIST, suf === 'index.html' ? path.join(c, 'index.html') : c + suf))) ||
    fs.existsSync(path.join(DIST, c)); // literal (e.g. 404.html)
};

function checkBuild() {
  if (!fs.existsSync(DIST)) fail('dist/ missing — run `npm run build` first');
  const rows = readTsv();
  const lost = [], pending = [];
  for (const r of rows) {
    if (r.status === 'dies') continue;
    const devServes = inDist(r.dev);
    const finalServes = inDist(r.tgt);
    if (!devServes && !finalServes) lost.push(r.dev);
    else if (r.tgt && !finalServes) pending.push(`${r.dev} → ${r.tgt} (final target not built yet)`);
  }
  if (pending.length) {
    console.log(`… ${pending.length} pending (final home not merged yet — expected until batches land):`);
    pending.slice(0, 15).forEach((p) => console.log(`   ${p}`));
  }
  if (lost.length) {
    console.error(`✗ ${lost.length} dev URL(s) resolve NOWHERE in dist — conservation broken:`);
    lost.forEach((p) => console.error(`   ${p}`));
    process.exit(1);
  }
  console.log(`✓ conservation holds: ${rows.length} mapped URLs all reachable in dist (or explicitly dying chrome)`);
}

function flippable() {
  const rows = readTsv();
  const byDev = new Map(rows.map((r) => [r.dev, r]));
  const hits = [];
  walk(path.join(ROOT, 'src', 'content', 'docs'), (f) => {
    if (!/\.(md|mdx)$/.test(f)) return;
    const text = fs.readFileSync(f, 'utf8');
    for (const m of text.matchAll(/https?:\/\/developers\.keboola\.com(\/[^\s)"'<>\]]*)/g)) {
      const p = clean(m[1].split('#')[0]);
      if (!p || p === '/') continue; // bare-domain links are a homepage-cutover call, not a mechanical flip
      const row = byDev.get(p);
      const target = row?.status === 'moved' || row?.status === 'redirect' ? row.tgt || p : null;
      if (target && inDist(target))
        hits.push(`${f.replace(ROOT + '/', '')}: ${m[0]} → ${target}`);
    }
  });
  if (!hits.length) return console.log('✓ no flippable dev-domain links (all remaining targets not migrated yet)');
  console.log(`${hits.length} link(s) can flip to internal now:`);
  hits.forEach((h) => console.log(`  ${h}`));
}

async function live() {
  const rows = readTsv().filter((r) => r.status !== 'dies');
  let bad = 0;
  for (const r of rows) {
    const res = await fetch(DEV_HOST + r.dev, { redirect: 'manual' }).catch(() => null);
    const loc = res?.headers.get('location') ?? '';
    const expected = HELP_HOST + (r.tgt || r.dev);
    const ok = res && [301, 308].includes(res.status) && (loc === expected || loc.replace(/\/$/, '') === expected.replace(/\/$/, ''));
    if (!ok) { bad++; console.error(`✗ ${r.dev}: got ${res?.status ?? 'ERR'} ${loc || ''} — want 301 ${expected}`); }
  }
  console.log(bad ? `✗ ${bad}/${rows.length} mismatches` : `✓ all ${rows.length} URLs 301 correctly`);
  if (bad) process.exit(1);
}

function walk(dir, fn) { for (const e of fs.readdirSync(dir, { withFileTypes: true })) { const p = path.join(dir, e.name); e.isDirectory() ? walk(p, fn) : fn(p); } }
function count(a) { return a.reduce((m, x) => (m[x] = (m[x] ?? 0) + 1, m), {}); }
function fmt(o) { return Object.entries(o).map(([k, v]) => `${v} ${k}`).join(', '); }
function fail(msg) { console.error(`✗ ${msg}`); process.exit(1); }

if (mode === '--gen') gen();
else if (mode === '--build') checkBuild();
else if (mode === '--flippable') flippable();
else if (mode === '--live') await live();
else fail('usage: check-redirects.mjs --gen | --build | --flippable | --live');
